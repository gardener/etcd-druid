// buildConfigMapWithEtcdConfig creates a ConfigMap with the etcd config YAML for the given Etcd.
// It mirrors the structure generated by internal/component/configmap/etcdconfig.go.
func buildConfigMapWithEtcdConfig(etcd *druidv1alpha1.Etcd) (*corev1.ConfigMap, error) {
	peerSvcName := druidv1alpha1.GetPeerServiceName(etcd.ObjectMeta)
	peerScheme := "http"
	if etcd.Spec.Etcd.PeerUrlTLS != nil {
		peerScheme = "https"
	}
	clientScheme := "http"
	if etcd.Spec.Etcd.ClientUrlTLS != nil {
		clientScheme = "https"
	}
	peerPort := common.DefaultPortEtcdPeer
	if etcd.Spec.Etcd.ServerPort != nil {
		peerPort = *etcd.Spec.Etcd.ServerPort
	}
	clientPort := common.DefaultPortEtcdClient
	if etcd.Spec.Etcd.ClientPort != nil {
		clientPort = *etcd.Spec.Etcd.ClientPort
	}
	initialCluster := ""
	for i := range int(etcd.Spec.Replicas) {
		podName := druidv1alpha1.GetOrdinalPodName(etcd.ObjectMeta, i)
		entry := fmt.Sprintf("%s=%s://%s.%s.%s.svc:%d",
			podName, peerScheme, podName, peerSvcName, etcd.Namespace, peerPort)
		if i > 0 {
			initialCluster += ","
		}
		initialCluster += entry
	}
	advertisePeerURLs := make(map[string][]string)
	advertiseClientURLs := make(map[string][]string)
	for i := range int(etcd.Spec.Replicas) {
		podName := druidv1alpha1.GetOrdinalPodName(etcd.ObjectMeta, i)
		advertisePeerURLs[podName] = []string{
			fmt.Sprintf("%s://%s.%s.%s.svc:%d", peerScheme, podName, peerSvcName, etcd.Namespace, peerPort),
		}
		advertiseClientURLs[podName] = []string{
			fmt.Sprintf("%s://%s.%s.%s.svc:%d", clientScheme, podName, peerSvcName, etcd.Namespace, clientPort),
		}
	}
	etcdCfg := map[string]interface{}{
		"name":                        "etcd-config",
		"initial-cluster":             initialCluster,
		"initial-cluster-state":       "new",
		"initial-advertise-peer-urls": advertisePeerURLs,
		"advertise-client-urls":       advertiseClientURLs,
		"listen-peer-urls":            fmt.Sprintf("%s://0.0.0.0:%d", peerScheme, peerPort),
		"listen-client-urls":          fmt.Sprintf("%s://0.0.0.0:%d", clientScheme, clientPort),
	}
	configBytes, err := yaml.Marshal(etcdCfg)
	if err != nil {
		return nil, fmt.Errorf("failed to marshal etcd config: %w", err)
	}
	return &corev1.ConfigMap{
		ObjectMeta: metav1.ObjectMeta{
			Name:      druidv1alpha1.GetConfigMapName(etcd.ObjectMeta),
			Namespace: etcd.Namespace,
		},
		Data: map[string]string{
			common.EtcdConfigFileName: string(configBytes),
		},
	}, nil
}

