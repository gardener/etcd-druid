// TestRecoverFromQuorumLossExecuteStepByStep verifies that each reconcile call advances
// exactly one step, records the step annotation on the Etcd CR, and that resuming Execute
// from an annotated state skips already-completed steps.
func TestRecoverFromQuorumLossExecuteStepByStep(t *testing.T) {
	g := NewGomegaWithT(t)
	const (
		etcdName  = "test-etcd"
		namespace = "test-ns"
	)

	etcd := createEtcdWithBackup(etcdName, namespace, 3, false)
	cm, err := buildConfigMapWithEtcdConfig(etcd)
	g.Expect(err).To(BeNil())
	sts := buildStatefulSet(etcd, 3, 3)
	pod := buildReadyPod(etcd)

	cl := utils.NewTestClientBuilder().WithScheme(kubernetes.Scheme).
		WithObjects(etcd, cm, sts, pod).Build()
	task := buildEtcdOpsTask(etcdName, namespace, nil)
	taskHandler, handlerErr := New(cl, task, nil)
	g.Expect(handlerErr).To(BeNil())

	ctx := context.Background()

	type stepExpect struct {
		step        recoveryStep
		description string
		// sideEffect is called after Execute() returns for this step, before the next Execute().
		// Use it to simulate external state changes that the fake client won't do automatically.
		sideEffect func()
	}
	// Each entry represents one reconcile invocation that should succeed and requeue.
	// sideEffect functions simulate controller-managed status changes the fake client won't do.
	expectations := []stepExpect{
		{step: stepSuspendReconciliation, description: "Suspended etcd-druid reconciliation"},
		{step: stepDisableComponentProtect, description: "Disabled etcd component protection"},
		{step: stepScaleDown, description: "Scaled StatefulSet down to 0 replicas", sideEffect: func() {
			// Simulate the StatefulSet controller driving replicas to 0 in status.
			sts := &appsv1.StatefulSet{}
			g.Expect(cl.Get(ctx, types.NamespacedName{Name: etcdName, Namespace: namespace}, sts)).To(Succeed())
			sts.Status.Replicas = 0
			sts.Status.ReadyReplicas = 0
			g.Expect(cl.Status().Update(ctx, sts)).To(Succeed())
		}},
		{step: stepWaitScaleDown, description: "StatefulSet scaled down to 0 replicas"},
		{step: stepDeletePVCs, description: "Deleted all PVCs"},
		{step: stepDeleteMemberLeases, description: "Deleted all member leases"},
		{step: stepPatchConfigMap, description: "Patched ConfigMap to single-member configuration"},
		{step: stepScaleUp, description: "Scaled StatefulSet up to 1 replica"},
		{step: stepWaitPodReady, description: "Single-member etcd pod is ready"},
		{step: stepReenableReconciliation, description: "Re-enabled etcd-druid reconciliation"},
	}

	for _, exp := range expectations {
		result := taskHandler.Execute(ctx)
		g.Expect(result.Error).To(BeNil(), "unexpected error at step %s", exp.step)
		g.Expect(result.Requeue).To(BeTrue(), "expected requeue after step %s", exp.step)
		g.Expect(result.Description).To(Equal(exp.description), "wrong description at step %s", exp.step)

		updatedEtcd := &druidv1alpha1.Etcd{}
		g.Expect(cl.Get(ctx, types.NamespacedName{Name: etcdName, Namespace: namespace}, updatedEtcd)).To(Succeed())
		g.Expect(updatedEtcd.Annotations).To(HaveKeyWithValue(recoveryStepAnnotation, string(exp.step)),
			"step annotation not recorded after step %s", exp.step)

		if exp.sideEffect != nil {
			exp.sideEffect()
		}
	}

	// Final step returns success with no requeue.
	result := taskHandler.Execute(ctx)
	g.Expect(result.Error).To(BeNil())
	g.Expect(result.Requeue).To(BeFalse())
	g.Expect(result.Description).To(Equal("Recovery from quorum loss completed successfully. " +
		"etcd-druid will reconcile and bring up the remaining cluster members."))
}

